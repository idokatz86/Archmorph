SUPPORTED_FORMATS = {"docx", "pdf", "pptx"}


def export_hld(hld: Dict[str, Any], format: str, include_diagrams: bool = True,
               diagram_b64: Optional[str] = None) -> Dict[str, Any]:
    """
    Export HLD document in the requested format.

    Returns dict with:
      - format: str
      - filename: str
      - content_b64: str (base64-encoded file bytes)
      - content_type: str (MIME type)
    """
    if format not in SUPPORTED_FORMATS:
        raise ValueError(f"Unsupported format: {format}. Use one of: {', '.join(sorted(SUPPORTED_FORMATS))}")

    title_slug = (hld.get("title", "archmorph-hld") or "archmorph-hld").lower()
    title_slug = "".join(c if c.isalnum() or c in "-_ " else "" for c in title_slug).strip().replace(" ", "-")[:50]
    if not title_slug.startswith("archmorph"):
        title_slug = f"archmorph-{title_slug}"

    exporters = {
        "docx": (export_hld_docx, f"{title_slug}.docx", "application/vnd.openxmlformats-officedocument.wordprocessingml.document"),
        "pdf":  (export_hld_pdf,  f"{title_slug}.pdf",  "application/pdf"),
        "pptx": (export_hld_pptx, f"{title_slug}.pptx", "application/vnd.openxmlformats-officedocument.presentationml.presentation"),
    }

    export_fn, filename, content_type = exporters[format]
    file_bytes = export_fn(hld, include_diagrams=include_diagrams, diagram_b64=diagram_b64)

    return {
        "format": format,
        "filename": filename,
        "content_b64": base64.b64encode(file_bytes).decode("ascii"),
        "content_type": content_type,
    }
