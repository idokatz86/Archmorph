# WORD (DOCX) EXPORT
# ═════════════════════════════════════════════════════════════
def export_hld_docx(hld: Dict[str, Any], include_diagrams: bool = True,
                    diagram_b64: Optional[str] = None) -> bytes:
    """Export HLD to a Word (.docx) document. Returns bytes."""
    from docx import Document
    from docx.shared import Inches, Pt, RGBColor
    from docx.enum.text import WD_ALIGN_PARAGRAPH
    from docx.enum.table import WD_TABLE_ALIGNMENT

    doc = Document()

    # ── Page style ──
    style = doc.styles["Normal"]
    font = style.font
    font.name = "Calibri"
    font.size = Pt(11)
    font.color.rgb = RGBColor(51, 65, 85)

    for level in range(1, 4):
        hs = doc.styles[f"Heading {level}"]
        hs.font.color.rgb = RGBColor(*BRAND_DARK)
        hs.font.name = "Calibri"

    # ── Title page ──
    title = doc.add_heading(hld.get("title", "High-Level Design Document"), level=0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = title.runs[0]
    run.font.size = Pt(28)
    run.font.color.rgb = RGBColor(*BRAND_GREEN)

    sub = doc.add_paragraph()
    sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
    sub_run = sub.add_run("Generated by Archmorph — AI-powered Cloud Architecture Translator")
    sub_run.font.size = Pt(11)
    sub_run.font.color.rgb = RGBColor(*DARK_GRAY)
    sub_run.font.italic = True

    # Metadata
    meta = hld.get("_metadata") or {}
    if meta:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        info_parts = []
        if meta.get("source_provider"):
            info_parts.append(f"Source: {meta['source_provider'].upper()}")
        if meta.get("services_count"):
            info_parts.append(f"{meta['services_count']} services")
        if meta.get("zones_count"):
            info_parts.append(f"{meta['zones_count']} zones")
        r = p.add_run(" | ".join(info_parts))
        r.font.size = Pt(10)
        r.font.color.rgb = RGBColor(*DARK_GRAY)

    doc.add_page_break()

    # ─── Helper to add styled section ───
    def add_section(heading: str, level: int = 1):
        h = doc.add_heading(heading, level=level)
        return h

    def add_kv(key: str, value: str):
        p = doc.add_paragraph()
        kr = p.add_run(f"{key}: ")
        kr.bold = True
        kr.font.size = Pt(11)
        p.add_run(_safe(value))

    def add_bullet_list(items: List[str]):
        for item in items:
            doc.add_paragraph(_safe(item), style="List Bullet")

    # ── 1. Executive Summary ──
    add_section("1. Executive Summary")
    doc.add_paragraph(_safe(hld.get("executive_summary")))

    # ── 2. Architecture Overview ──
    arch = hld.get("architecture_overview") or {}
    add_section("2. Architecture Overview")
    add_kv("Architecture Style", arch.get("architecture_style"))
    add_kv("Deployment Model", arch.get("deployment_model"))
    if arch.get("description"):
        doc.add_paragraph(arch["description"])
    if arch.get("diagram_description"):
        add_section("Data Flow", level=2)
        doc.add_paragraph(arch["diagram_description"])

    # Insert architecture diagram if provided
    if include_diagrams and diagram_b64:
        img_bytes = _decode_diagram_image(diagram_b64)
        if img_bytes:
            add_section("Architecture Diagram", level=2)
            stream = io.BytesIO(img_bytes)
            doc.add_picture(stream, width=Inches(6))
            last_para = doc.paragraphs[-1]
            last_para.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # ── 3. Services ──
    services = hld.get("services", [])
    if services:
        add_section("3. Azure Services — Detailed Design")
        for i, svc in enumerate(services, 1):
            add_section(f"3.{i}. {_safe(svc.get('azure_service'))}", level=2)
            if svc.get("source_service"):
                add_kv("Replaces", svc["source_service"])
            if svc.get("description"):
                doc.add_paragraph(svc["description"])
            add_kv("Justification", svc.get("justification"))
            if svc.get("alternatives_considered"):
                add_kv("Alternatives Considered", ", ".join(svc["alternatives_considered"]))
            add_kv("Recommended Tier", svc.get("tier_recommendation"))
            add_kv("SLA", svc.get("sla"))
            add_kv("Estimated Cost", svc.get("estimated_monthly_cost"))

            comm = svc.get("communication", {})
            if comm.get("connects_to"):
                add_kv("Connects To", f"{', '.join(comm['connects_to'])} ({_safe(comm.get('protocol'))} — {_safe(comm.get('pattern'))})")

            if svc.get("limitations"):
                p = doc.add_paragraph()
                p.add_run("Limitations:").bold = True
                add_bullet_list(svc["limitations"])

    # ── 4. Networking ──
    net = hld.get("networking_design") or {}
    if net:
        add_section("4. Networking Design")
        for field, label in [("topology", "Topology"), ("vnet_design", "VNet Design"),
                             ("connectivity", "Connectivity"), ("dns_strategy", "DNS Strategy")]:
            if net.get(field):
                add_kv(label, net[field])
        if net.get("security_controls"):
            add_kv("Security Controls", ", ".join(net["security_controls"]))
        if net.get("recommendations"):
            p = doc.add_paragraph()
            p.add_run("Recommendations:").bold = True
            add_bullet_list(net["recommendations"])

    # ── 5. Security ──
    sec = hld.get("security_design") or {}
    if sec:
        add_section("5. Security Design")
        for field, label in [("identity", "Identity & Access"), ("data_protection", "Data Protection"),
                             ("network_security", "Network Security"), ("secrets_management", "Secrets Management")]:
            if sec.get(field):
                add_kv(label, sec[field])
        if sec.get("compliance_frameworks"):
            add_kv("Compliance Frameworks", ", ".join(sec["compliance_frameworks"]))

    # ── 6. Data Architecture ──
    data = hld.get("data_architecture") or {}
    if data:
        add_section("6. Data Architecture")
        for field, label in [("data_flow", "Data Flow"), ("storage_strategy", "Storage Strategy"),
                             ("database_strategy", "Database Strategy"), ("data_residency", "Data Residency"),
                             ("backup_and_recovery", "Backup & Recovery")]:
            if data.get(field):
                add_kv(label, data[field])

    # ── 7. CAF Alignment ──
    caf = hld.get("azure_caf_alignment") or {}
    if caf:
        add_section("7. Azure Cloud Adoption Framework Alignment")
        for field, label in [("landing_zone", "Landing Zone"), ("management_groups", "Management Groups"),
                             ("subscription_design", "Subscription Design"), ("naming_convention", "Naming Convention"),
                             ("tagging_strategy", "Tagging Strategy"), ("resource_organization", "Resource Organization")]:
            if caf.get(field):
                add_kv(label, caf[field])

    # ── 8. FinOps ──
    fin = hld.get("finops") or {}
    if fin:
        add_section("8. FinOps — Cost Management")
        add_kv("Total Estimated Monthly Cost", fin.get("total_estimated_monthly_cost"))
        if fin.get("cost_optimization_recommendations"):
            p = doc.add_paragraph()
            p.add_run("Cost Optimization Recommendations:").bold = True
            add_bullet_list(fin["cost_optimization_recommendations"])
        if fin.get("reserved_instances_candidates"):
            add_kv("Reserved Instance Candidates", ", ".join(fin["reserved_instances_candidates"]))
        if fin.get("cost_monitoring"):
            add_kv("Cost Monitoring", fin["cost_monitoring"])

    # ── 9. Region Strategy ──
    region = hld.get("region_strategy") or {}
    if region:
        add_section("9. Region Strategy")
        add_kv("Primary Region", region.get("primary_region"))
        add_kv("DR Region", region.get("dr_region"))
        if region.get("region_selection_factors"):
            add_kv("Selection Factors", ", ".join(region["region_selection_factors"]))
        if region.get("data_residency_considerations"):
            add_kv("Data Residency", region["data_residency_considerations"])

    # ── 10. WAF Assessment ──
    waf = hld.get("waf_assessment") or {}
    if waf:
        add_section("10. Well-Architected Framework Assessment")
        table = doc.add_table(rows=1, cols=3)
        table.style = "Light Grid Accent 1"
        table.alignment = WD_TABLE_ALIGNMENT.CENTER
        hdr = table.rows[0].cells
        hdr[0].text = "Pillar"
        hdr[1].text = "Score"
        hdr[2].text = "Notes"
        for pillar in ["reliability", "security", "cost_optimization", "operational_excellence", "performance_efficiency"]:
            p = waf.get(pillar, {})
            row = table.add_row().cells
            row[0].text = pillar.replace("_", " ").title()
            row[1].text = _safe(p.get("score"))
            row[2].text = _safe(p.get("notes"), "")

    # ── 11. Migration Roadmap ──
    mig = hld.get("migration_approach") or {}
    if mig:
        add_section("11. Migration Roadmap")
        add_kv("Strategy", mig.get("strategy"))
        for phase in mig.get("phases", []):
            add_section(f"Phase {phase.get('phase', '?')}: {_safe(phase.get('name'))}", level=2)
            if phase.get("description"):
                doc.add_paragraph(phase["description"])
            if phase.get("services"):
                add_kv("Services", ", ".join(phase["services"]))
            add_kv("Duration", f"{phase.get('duration_weeks', '?')} weeks")
            if phase.get("risks"):
                add_kv("Risks", ", ".join(phase["risks"]))
        if mig.get("rollback_plan"):
            add_kv("Rollback Plan", mig["rollback_plan"])
        if mig.get("testing_strategy"):
            add_kv("Testing Strategy", mig["testing_strategy"])

    # ── 12. Considerations ──
    considerations = hld.get("considerations", [])
    if considerations:
        add_section("12. Key Considerations")
        add_bullet_list(considerations)

    # ── 13. Risks & Mitigations ──
    risks = hld.get("risks_and_mitigations", [])
    if risks:
        add_section("13. Risks & Mitigations")
        table = doc.add_table(rows=1, cols=3)
        table.style = "Light Grid Accent 1"
        table.alignment = WD_TABLE_ALIGNMENT.CENTER
        hdr = table.rows[0].cells
        hdr[0].text = "Risk"
        hdr[1].text = "Impact"
        hdr[2].text = "Mitigation"
        for r in risks:
            row = table.add_row().cells
            row[0].text = _safe(r.get("risk"), "")
            row[1].text = _safe(r.get("impact"), "")
            row[2].text = _safe(r.get("mitigation"), "")

    # ── 14. Next Steps ──
    next_steps = hld.get("next_steps", [])
    if next_steps:
        add_section("14. Next Steps")
        for i, s in enumerate(next_steps, 1):
            doc.add_paragraph(f"{i}. {s}")

    # Footer
    doc.add_paragraph("")
    footer = doc.add_paragraph()
    footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = footer.add_run(f"Generated by Archmorph v{__version__}")
    r.font.size = Pt(9)
    r.font.color.rgb = RGBColor(*DARK_GRAY)
    r.font.italic = True

    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()


