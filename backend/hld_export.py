def export_hld_pptx(hld: Dict[str, Any], include_diagrams: bool = True,
                    diagram_b64: Optional[str] = None) -> bytes:
    """Export HLD to a PowerPoint (.pptx) presentation. Returns bytes."""
    from pptx import Presentation
    from pptx.util import Inches, Pt
    from pptx.dml.color import RGBColor as PptxRGB

    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    def add_slide(title_text: str, subtitle_text: str = ""):
        slide = prs.slides.add_slide(prs.slide_layouts[6])  # blank layout

        # Dark background
        bg = slide.background
        fill = bg.fill
        fill.solid()
        fill.fore_color.rgb = PptxRGB(*BRAND_DARK)

        # Title
        left = Inches(0.8)
        top = Inches(0.5)
        txBox = slide.shapes.add_textbox(left, top, Inches(11.5), Inches(0.8))
        tf = txBox.text_frame
        tf.word_wrap = True
        p = tf.paragraphs[0]
        p.text = title_text
        p.font.size = Pt(28)
        p.font.bold = True
        p.font.color.rgb = PptxRGB(*BRAND_GREEN)

        if subtitle_text:
            p2 = tf.add_paragraph()
            p2.text = subtitle_text
            p2.font.size = Pt(14)
            p2.font.color.rgb = PptxRGB(*DARK_GRAY)

        return slide

    def add_content_box(slide, text: str, left: float = 0.8, top: float = 1.5,
                        width: float = 11.5, height: float = 5.0, font_size: int = 14):
        txBox = slide.shapes.add_textbox(Inches(left), Inches(top), Inches(width), Inches(height))
        tf = txBox.text_frame
        tf.word_wrap = True

        for line in text.split("\n"):
            p = tf.add_paragraph()
            line = line.strip()
            if line.startswith("**") and line.endswith("**"):
                p.text = line.strip("*")
                p.font.bold = True
                p.font.size = Pt(font_size + 2)
                p.font.color.rgb = PptxRGB(*BRAND_GREEN)
            elif line.startswith("• ") or line.startswith("- "):
                p.text = line
                p.font.size = Pt(font_size)
                p.font.color.rgb = PptxRGB(*WHITE)
                p.level = 1
            else:
                p.text = line
                p.font.size = Pt(font_size)
                p.font.color.rgb = PptxRGB(*WHITE)
        # Remove empty first paragraph
        if tf.paragraphs and tf.paragraphs[0].text == "":
            tf.paragraphs[0]._p.getparent().remove(tf.paragraphs[0]._p)
        return txBox

    def kv_lines(pairs: List[tuple]) -> str:
        return "\n".join(f"**{k}**\n{_safe(v)}\n" for k, v in pairs if v)

    # ── Slide 1: Title ──
    title_slide = add_slide(
        hld.get("title", "High-Level Design Document"),
        "Generated by Archmorph — AI-powered Cloud Architecture Translator"
    )
    meta = hld.get("_metadata") or {}
    if meta:
        info_parts = []
        if meta.get("source_provider"):
            info_parts.append(f"Source: {meta['source_provider'].upper()}")
        if meta.get("services_count"):
            info_parts.append(f"{meta['services_count']} services")
        if meta.get("zones_count"):
            info_parts.append(f"{meta['zones_count']} zones")
        if info_parts:
            add_content_box(title_slide, " | ".join(info_parts), top=2.5, font_size=16)

    # ── Slide 2: Executive Summary ──
    s = add_slide("Executive Summary")
    add_content_box(s, _safe(hld.get("executive_summary")))

    # ── Slide 3: Architecture Overview ──
    arch = hld.get("architecture_overview") or {}
    s = add_slide("Architecture Overview")
    content = kv_lines([
        ("Architecture Style", arch.get("architecture_style")),
        ("Deployment Model", arch.get("deployment_model")),
        ("Description", arch.get("description")),
    ])
    add_content_box(s, content)

    # ── Slide: Architecture Diagram (optional) ──
    if include_diagrams and diagram_b64:
        img_bytes = _decode_diagram_image(diagram_b64)
        if img_bytes:
            s = add_slide("Architecture Diagram")
            stream = io.BytesIO(img_bytes)
            try:
                s.shapes.add_picture(stream, Inches(1.5), Inches(1.5), width=Inches(10))
            except Exception:
                add_content_box(s, "[Diagram could not be embedded]")

    # ── Slide 4: Services (split into chunks of 3) ──
    services = hld.get("services", [])
    for chunk_start in range(0, len(services), 3):
        chunk = services[chunk_start:chunk_start + 3]
        page_num = chunk_start // 3 + 1
        s = add_slide(f"Azure Services (Page {page_num})")
        lines = []
        for svc in chunk:
            lines.append(f"**{_safe(svc.get('azure_service'))}**")
            if svc.get("source_service"):
                lines.append(f"Replaces: {svc['source_service']}")
            lines.append(f"Tier: {_safe(svc.get('tier_recommendation'))} | SLA: {_safe(svc.get('sla'))} | Cost: {_safe(svc.get('estimated_monthly_cost'))}")
            lines.append(f"{_safe(svc.get('justification'), '')}")
            lines.append("")
        add_content_box(s, "\n".join(lines), font_size=12)

    # ── Slide: Networking ──
    net = hld.get("networking_design") or {}
    if net:
        s = add_slide("Networking Design")
        add_content_box(s, kv_lines([
            ("Topology", net.get("topology")),
            ("VNet Design", net.get("vnet_design")),
            ("DNS Strategy", net.get("dns_strategy")),
            ("Security Controls", ", ".join(net.get("security_controls", []))),
        ]))

    # ── Slide: Security ──
    sec = hld.get("security_design") or {}
    if sec:
        s = add_slide("Security Design")
        add_content_box(s, kv_lines([
            ("Identity & Access", sec.get("identity")),
            ("Data Protection", sec.get("data_protection")),
            ("Network Security", sec.get("network_security")),
            ("Secrets Management", sec.get("secrets_management")),
        ]))

    # ── Slide: FinOps ──
    fin = hld.get("finops") or {}
    if fin:
        s = add_slide("FinOps — Cost Management")
        content = f"**Total Estimated Monthly Cost**\n{_safe(fin.get('total_estimated_monthly_cost'))}\n\n"
        if fin.get("cost_optimization_recommendations"):
            content += "**Cost Optimization**\n"
            content += "\n".join(f"• {r}" for r in fin["cost_optimization_recommendations"])
        add_content_box(s, content)

    # ── Slide: WAF Assessment ──
    waf = hld.get("waf_assessment") or {}
    if waf:
        s = add_slide("Well-Architected Framework Assessment")
        lines = []
        for pillar in ["reliability", "security", "cost_optimization", "operational_excellence", "performance_efficiency"]:
            p = waf.get(pillar, {})
            lines.append(f"**{pillar.replace('_', ' ').title()}** — {_safe(p.get('score'))}")
            lines.append(f"{_safe(p.get('notes'), '')}")
            lines.append("")
        add_content_box(s, "\n".join(lines), font_size=13)

    # ── Slide: Migration Roadmap ──
    mig = hld.get("migration_approach") or {}
    if mig:
        s = add_slide("Migration Roadmap", f"Strategy: {_safe(mig.get('strategy'))}")
        lines = []
        for phase in mig.get("phases", []):
            lines.append(f"**Phase {phase.get('phase', '?')}: {_safe(phase.get('name'))}**")
            if phase.get("services"):
                lines.append(f"Services: {', '.join(phase['services'])}")
            lines.append(f"Duration: {phase.get('duration_weeks', '?')} weeks")
            lines.append("")
        add_content_box(s, "\n".join(lines), font_size=13)

    # ── Slide: Next Steps ──
    next_steps = hld.get("next_steps", [])
    if next_steps:
        s = add_slide("Next Steps")
        content = "\n".join(f"{i}. {step}" for i, step in enumerate(next_steps, 1))
        add_content_box(s, content)

    buf = io.BytesIO()
    prs.save(buf)
    return buf.getvalue()


