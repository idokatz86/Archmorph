LIGHT_GRAY = (226, 232, 240)      # #E2E8F0
DARK_GRAY = (100, 116, 139)       # #64748B


# ─────────────────────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────────────────────
def _safe(val: Any, default: str = "N/A") -> str:
    """Safely convert a value to string."""
    if val is None:
        return default
    return str(val)


def _pdf_safe(text: str) -> str:
    """Sanitize text for PDF rendering with built-in Helvetica (latin-1 only).

    Replaces common Unicode characters with ASCII equivalents so fpdf2
    can render them without requiring a TrueType Unicode font.
    """
    replacements = {
        "\u2014": "--",   # em-dash
        "\u2013": "-",    # en-dash
        "\u2018": "'",    # left single quote
        "\u2019": "'",    # right single quote
        "\u201c": '"',    # left double quote
        "\u201d": '"',    # right double quote
        "\u2026": "...",  # ellipsis
        "\u2022": "-",    # bullet
        "\u2192": "->",   # right arrow
        "\u2190": "<-",   # left arrow
        "\u00a0": " ",    # non-breaking space
        "\u00b7": "-",    # middle dot
        "\u2502": "|",    # box drawing vertical
        "\u251c": "|",    # box drawing tee
        "\u2500": "-",    # box drawing horizontal
        "\u2514": "`",    # box drawing corner
        "\u25b6": ">",    # right-pointing triangle
    }
    for char, repl in replacements.items():
        text = text.replace(char, repl)
    # Strip any remaining non-latin-1 characters
    return text.encode("latin-1", errors="replace").decode("latin-1")


def _decode_diagram_image(diagram_b64: Optional[str]) -> Optional[bytes]:
    """Decode a base64-encoded diagram image, if provided."""
    if not diagram_b64:
        return None
    try:
        # Strip data URI prefix if present
        if "," in diagram_b64:
            diagram_b64 = diagram_b64.split(",", 1)[1]
        return base64.b64decode(diagram_b64)
    except Exception:
        logger.warning("Failed to decode diagram image")
        return None


