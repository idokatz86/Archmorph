# PDF EXPORT
# ═════════════════════════════════════════════════════════════
def export_hld_pdf(hld: Dict[str, Any], include_diagrams: bool = True,
                   diagram_b64: Optional[str] = None) -> bytes:
    """Export HLD to a PDF document. Returns bytes."""
    from fpdf import FPDF

    class ArchmorphPDF(FPDF):
        def header(self):
            self.set_font("Helvetica", "I", 8)
            self.set_text_color(*DARK_GRAY)
            self.cell(0, 6, _pdf_safe(hld.get("title", "Archmorph HLD")), align="L")
            self.ln(8)

        def footer(self):
            self.set_y(-15)
            self.set_font("Helvetica", "I", 8)
            self.set_text_color(*DARK_GRAY)
            self.cell(0, 10, _pdf_safe(f"Page {self.page_no()}/{{nb}} -- Generated by Archmorph v{__version__}"), align="C")

        def section_heading(self, text: str, level: int = 1):
            sizes = {1: 16, 2: 13, 3: 11}
            self.set_font("Helvetica", "B", sizes.get(level, 11))
            self.set_text_color(*BRAND_DARK)
            self.ln(4)
            self.multi_cell(0, 7, _pdf_safe(text))
            self.ln(2)

        def body_text(self, text: str):
            self.set_font("Helvetica", "", 10)
            self.set_text_color(51, 65, 85)
            self.multi_cell(0, 5, _pdf_safe(text))
            self.ln(1)

        def key_value(self, key: str, value: str):
            key = _pdf_safe(key)
            value = _pdf_safe(value)
            self.set_font("Helvetica", "B", 10)
            self.set_text_color(*BRAND_DARK)
            w = self.get_string_width(f"{key}: ") + 2
            self.cell(w, 5, f"{key}: ")
            self.set_font("Helvetica", "", 10)
            self.set_text_color(51, 65, 85)
            self.multi_cell(0, 5, value)
            self.ln(1)

        def bullet_list(self, items: List[str]):
            self.set_font("Helvetica", "", 10)
            self.set_text_color(51, 65, 85)
            for item in items:
                self.multi_cell(0, 5, _pdf_safe(f"  - {item}"))
                self.ln(0.5)

    pdf = ArchmorphPDF()
    pdf.alias_nb_pages()
    pdf.set_auto_page_break(auto=True, margin=20)
    pdf.add_page()

    # ── Title page ──
    pdf.ln(40)
    pdf.set_font("Helvetica", "B", 28)
    pdf.set_text_color(*BRAND_GREEN)
    pdf.multi_cell(0, 12, _pdf_safe(hld.get("title", "High-Level Design Document")), align="C")
    pdf.ln(6)

    pdf.set_font("Helvetica", "I", 12)
    pdf.set_text_color(*DARK_GRAY)
    pdf.cell(0, 8, "Generated by Archmorph", align="C")
    pdf.ln(8)
    pdf.cell(0, 6, "AI-powered Cloud Architecture Translator", align="C")
    pdf.ln(20)

    meta = hld.get("_metadata") or {}
    if meta:
        info_parts = []
        if meta.get("source_provider"):
            info_parts.append(f"Source: {meta['source_provider'].upper()}")
        if meta.get("services_count"):
            info_parts.append(f"{meta['services_count']} services")
        if meta.get("zones_count"):
            info_parts.append(f"{meta['zones_count']} zones")
        if info_parts:
            pdf.set_font("Helvetica", "", 10)
            pdf.set_text_color(*DARK_GRAY)
            pdf.cell(0, 6, " | ".join(info_parts), align="C")

    pdf.add_page()

    # ── 1. Executive Summary ──
    pdf.section_heading("1. Executive Summary")
    pdf.body_text(_safe(hld.get("executive_summary")))

    # ── 2. Architecture Overview ──
    arch = hld.get("architecture_overview") or {}
    pdf.section_heading("2. Architecture Overview")
    pdf.key_value("Architecture Style", _safe(arch.get("architecture_style")))
    pdf.key_value("Deployment Model", _safe(arch.get("deployment_model")))
    if arch.get("description"):
        pdf.body_text(arch["description"])

    if include_diagrams and diagram_b64:
        img_bytes = _decode_diagram_image(diagram_b64)
        if img_bytes:
            pdf.section_heading("Architecture Diagram", level=2)
            img_stream = io.BytesIO(img_bytes)
            # Try to get image dimensions — fit to page width
            try:
                pdf.image(img_stream, x=15, w=180)
            except Exception:
                pdf.body_text("[Diagram image could not be embedded]")
            pdf.ln(5)

    # ── 3. Services ──
    services = hld.get("services", [])
    if services:
        pdf.section_heading("3. Azure Services — Detailed Design")
        for i, svc in enumerate(services, 1):
            pdf.section_heading(f"3.{i}. {_safe(svc.get('azure_service'))}", level=2)
            if svc.get("source_service"):
                pdf.key_value("Replaces", svc["source_service"])
            if svc.get("description"):
                pdf.body_text(svc["description"])
            pdf.key_value("Justification", _safe(svc.get("justification")))
            pdf.key_value("Tier", _safe(svc.get("tier_recommendation")))
            pdf.key_value("SLA", _safe(svc.get("sla")))
            pdf.key_value("Est. Cost", _safe(svc.get("estimated_monthly_cost")))

    # ── 4. Networking ──
    net = hld.get("networking_design") or {}
    if net:
        pdf.section_heading("4. Networking Design")
        for field, label in [("topology", "Topology"), ("vnet_design", "VNet Design"),
                             ("dns_strategy", "DNS Strategy")]:
            if net.get(field):
                pdf.key_value(label, net[field])
        if net.get("security_controls"):
            pdf.key_value("Security Controls", ", ".join(net["security_controls"]))

    # ── 5. Security ──
    sec = hld.get("security_design") or {}
    if sec:
        pdf.section_heading("5. Security Design")
        for field, label in [("identity", "Identity & Access"), ("data_protection", "Data Protection"),
                             ("network_security", "Network Security"), ("secrets_management", "Secrets")]:
            if sec.get(field):
                pdf.key_value(label, sec[field])

    # ── 6. Data Architecture ──
    data = hld.get("data_architecture") or {}
    if data:
        pdf.section_heading("6. Data Architecture")
        for field, label in [("data_flow", "Data Flow"), ("storage_strategy", "Storage"),
                             ("database_strategy", "Database"), ("backup_and_recovery", "Backup & Recovery")]:
            if data.get(field):
                pdf.key_value(label, data[field])

    # ── 7. CAF Alignment ──
    caf = hld.get("azure_caf_alignment") or {}
    if caf:
        pdf.section_heading("7. Azure CAF Alignment")
        for field, label in [("landing_zone", "Landing Zone"), ("naming_convention", "Naming"),
                             ("tagging_strategy", "Tagging"), ("subscription_design", "Subscription")]:
            if caf.get(field):
                pdf.key_value(label, caf[field])

    # ── 8. FinOps ──
    fin = hld.get("finops") or {}
    if fin:
        pdf.section_heading("8. FinOps — Cost Management")
        pdf.key_value("Total Monthly Cost", _safe(fin.get("total_estimated_monthly_cost")))
        if fin.get("cost_optimization_recommendations"):
            pdf.body_text("Cost Optimization:")
            pdf.bullet_list(fin["cost_optimization_recommendations"])

    # ── 9. Region Strategy ──
    region = hld.get("region_strategy") or {}
    if region:
        pdf.section_heading("9. Region Strategy")
        pdf.key_value("Primary Region", _safe(region.get("primary_region")))
        pdf.key_value("DR Region", _safe(region.get("dr_region")))

    # ── 10. WAF Assessment ──
    waf = hld.get("waf_assessment") or {}
    if waf:
        pdf.section_heading("10. Well-Architected Framework Assessment")
        # Simple table
        pdf.set_font("Helvetica", "B", 10)
        pdf.set_fill_color(*LIGHT_GRAY)
        pdf.cell(55, 7, "Pillar", 1, fill=True)
        pdf.cell(25, 7, "Score", 1, fill=True)
        pdf.cell(0, 7, "Notes", 1, fill=True)
        pdf.ln()
        pdf.set_font("Helvetica", "", 9)
        pdf.set_text_color(51, 65, 85)
        for pillar in ["reliability", "security", "cost_optimization", "operational_excellence", "performance_efficiency"]:
            p = waf.get(pillar, {})
            pdf.cell(55, 6, pillar.replace("_", " ").title(), 1)
            pdf.cell(25, 6, _safe(p.get("score")), 1)
            pdf.cell(0, 6, _safe(p.get("notes"), "")[:60], 1)
            pdf.ln()

    # ── 11. Migration ──
    mig = hld.get("migration_approach") or {}
    if mig:
        pdf.section_heading("11. Migration Roadmap")
        pdf.key_value("Strategy", _safe(mig.get("strategy")))
        for phase in mig.get("phases", []):
            pdf.section_heading(f"Phase {phase.get('phase', '?')}: {_safe(phase.get('name'))}", level=2)
            if phase.get("description"):
                pdf.body_text(phase["description"])
            if phase.get("services"):
                pdf.key_value("Services", ", ".join(phase["services"]))
            pdf.key_value("Duration", f"{phase.get('duration_weeks', '?')} weeks")

    # ── 12-14. Considerations, Risks, Next Steps ──
    considerations = hld.get("considerations", [])
    if considerations:
        pdf.section_heading("12. Key Considerations")
        pdf.bullet_list(considerations)

    risks = hld.get("risks_and_mitigations", [])
    if risks:
        pdf.section_heading("13. Risks & Mitigations")
        for r in risks:
            pdf.key_value(_safe(r.get("risk"), "Risk"), f"Impact: {_safe(r.get('impact'), '-')} | Mitigation: {_safe(r.get('mitigation'), '-')}")

    next_steps = hld.get("next_steps", [])
    if next_steps:
        pdf.section_heading("14. Next Steps")
        for i, s in enumerate(next_steps, 1):
            pdf.body_text(f"{i}. {s}")

    return bytes(pdf.output())


