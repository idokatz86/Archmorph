import io
import logging
from typing import Any, Dict, List, Optional

from version import __version__

logger = logging.getLogger(__name__)

# ─────────────────────────────────────────────────────────────
# Color palette (matching Archmorph UI theme)
# ─────────────────────────────────────────────────────────────
BRAND_GREEN = (34, 197, 94)       # #22C55E — CTA green
BRAND_DARK = (15, 23, 42)         # #0F172A — primary dark
BRAND_SURFACE = (2, 6, 23)        # #020617 — surface
HEADING_BLUE = (59, 130, 246)     # #3B82F6 — info blue
